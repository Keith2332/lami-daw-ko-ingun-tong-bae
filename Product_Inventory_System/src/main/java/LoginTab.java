    private boolean doesUserExist(String filePath) {
        return Files.exists(Paths.get(filePath));
    }

    private boolean isPasswordCorrect(String filePath, String enteredPassword) {
        try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = reader.readLine()) != null) {
                if (line.startsWith("Password: ")) {
                    // Extract the stored password hash from the file
                    String storedPasswordHash = line.substring("Password: ".length());
                    
                    // Replace this line with actual password verification logic
                    return storedPasswordHash.equals(hashPassword(enteredPassword));
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return false;
    }
