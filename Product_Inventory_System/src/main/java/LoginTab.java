    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String email = txtEmail.getText();
    String password = txtPassword.getText();

    // Validate email and password
    if (email.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both email and password.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Check if the user exists
    String filePath = "D:\\Documents\\NetBeansProjects\\Product_Inventory_System\\USERS\\" + email + ".txt";
    if (!doesUserExist(filePath)) {
        JOptionPane.showMessageDialog(this, "Invalid email or password.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Verify the password
    if (!isPasswordCorrect(filePath, password)) {
        JOptionPane.showMessageDialog(this, "Invalid email or password.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Successful login, transition to MenuTab
    MenuTab menuTab = new MenuTab(email); // Pass the email or username here
    menuTab.show();
    this.hide();
    }//GEN-LAST:event_btnLoginActionPerformed
    private void addCheckboxListener() {
        chkShowPassword.addItemListener(e -> {
            if (e.getStateChange() == ItemEvent.SELECTED) {
                // Checkbox is checked, so unhide password field
                txtPassword.setEchoChar((char) 0);
            } else {
                // Checkbox is unchecked, so hide password field
                txtPassword.setEchoChar('*');
            }
        });
    }
